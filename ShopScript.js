  function displayProducts(category, sortOrder = 'asc') {
    const productList = document.getElementById('product-list');
    productList.innerHTML = '';
    let products;
  
    if (category === 'All Categories') {
      products = [].concat(...Object.values(data));
    } else {
      products = data[category];
    }
  
    products.sort((a, b) => {
      if (sortOrder === 'asc') {
        return a.name.localeCompare(b.name);
      } else {
        return b.name.localeCompare(a.name);
      }
    });
  
    products.forEach(product => {
      const productDiv = document.createElement('div');
      productDiv.className = 'product';
  
      const img = document.createElement('img');
      img.src = product.image;
      productDiv.appendChild(img);
  
      const productDetails = document.createElement('div');
      productDetails.className = 'product-details';
  
      const name = document.createElement('h3');
      name.textContent = product.name;
      productDetails.appendChild(name);
  
      const brand = document.createElement('p');
      brand.textContent = `Brand: ${product.brand}`;
      productDetails.appendChild(brand);
  
      const price = document.createElement('p');
      price.textContent = `Price: $${product.price}`;
      productDetails.appendChild(price);
  
      const description = document.createElement('p');
      description.textContent = product.description;
      productDetails.appendChild(description);
  
      const buyButton = document.createElement('button');
      buyButton.className = 'buy-button';
      buyButton.textContent = 'Kup teraz';
      buyButton.onclick = () => alert(`Kupiono: ${product.name}`);
      productDetails.appendChild(buyButton);
  
      productDiv.appendChild(productDetails);
      productList.appendChild(productDiv);
    });
  
    const sortButtons = document.createElement('div');
    sortButtons.className = 'sort-buttons';
  
    const ascButton = document.createElement('button');
    ascButton.textContent = 'Sort Ascending';
    ascButton.onclick = () => displayProducts(category, 'asc');
    sortButtons.appendChild(ascButton);
  
    const descButton = document.createElement('button');
    descButton.textContent = 'Sort Descending';
    descButton.onclick = () => displayProducts(category, 'desc');
    sortButtons.appendChild(descButton);
  
    productList.insertBefore(sortButtons, productList.firstChild);
  }
  
